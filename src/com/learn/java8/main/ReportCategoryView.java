package com.learn.java8.main;

public class ReportCategoryView {
	private String reportCat;
	private String country;
	private String reportCategoryStatus;
	
	public String getReportCat() {
		return reportCat;
	}
	public void setReportCat(String reportCat) {
		this.reportCat = reportCat;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getReportCategoryStatus() {
		return reportCategoryStatus;
	}
	public void setReportCategoryStatus(String reportCategoryStatus) {
		this.reportCategoryStatus = reportCategoryStatus;
	}
	
	
}
